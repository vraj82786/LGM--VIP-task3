            <span class="heading">About </span>
            <p>In 1949, there were 18 affiliated colleges (including colleges such as the Fergusson College, Sir
                Parashurambhau College, Nowrosjee Wadia College and College of Engineering, Pune) with an enrolment
                of over 8000. Thereafter, the number of colleges increased. As of 2017 the university had 43
                departments,[9] both for undergraduate and graduate courses.</p>
            <p>Savitribai Phule Pune University, one of the premier universities in India, is positioned in the
                North-western part of Pune city. It occupies an area of about 411 acres. It was established on 10th
                February, 1949 under the Poona University Act. The university houses 46 academic departments. It is
                popularly known as the 'Oxford of the East'. It has about 307 recognized research institutes and 612
                affiliated colleges offering graduate and under-graduate courses. </p>
