            <span class="heading">History</span>
            <p>University of Pune was established on 10 February 1949 under the Pune University Act, passed by the
                Bombay legislature in 1948.[5] M. R. Jayakar became its first Vice-Chancellor. Its first office was
                started from the Nizam Guest House, which is part of Bhandarkar Oriental Research Institute on Law
                College Road. The university was operated at Nizam Guest House till 1 June 1949. Its current
                building originally called the Governor House. As its name suggests, it was the seasonal retreat of
                the Governor of Bombay.[6] B. G. Kher, Chief Minister and Education Minister of the government of
                Bombay, helped ensure the university received a large allocation of land for their campus. The
                university was allocated over 411 acres (1.66 km2).[5]</p>
            <p>British The institution's name was changed from the University of Pune to Savitribai Phule Pune
                University
                on 9 August 2014 in honor of Savitribai Phule, the 19th century Indian social reformer who played an
                important role in improving the lives of women and the Dalit communities in Maharashtra during
                British colonial rule. She and her husband Mahatma Jyotirao Phule founded India's first native-run
                school for girls in 1848.</p>
